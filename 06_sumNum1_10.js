console.log("write a program in javascript to count total number of sum from 1 to 10 using function expression");
var sum = function () {
    var result = 0;
    for (var i = 1; i <= 10; i++) {
        result += i;
    }
    console.log(`total number of sum from 1 to 10 using function expression : ${result}`);
}
